import turtle    

t1=turtle.Turtle()
t2=turtle.Turtle()

t1.penup()
t2.penup()
t1.goto(-200,0)
t2.goto(200,0)
t1.pendown()
t2.pendown()

t1.forward(100)
t2.forward(100)
t1.left(90)
t2.left(90)

t1.forward(100)
t2.forward(100)
t1.left(90)
t2.left(90)

t1.forward(100)
t2.forward(100)
t1.left(90)
t2.left(90)

t1.forward(100)
t2.forward(100)

turtle.done()